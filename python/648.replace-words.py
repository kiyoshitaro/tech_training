class TrieNode:
    def __init__(self):
        self.children = {}
        self.endWord = False
class Trie:
    def __init__(self):
        self.root = TrieNode()
    def insert(self, word):
        curr = self.root
        for c in word:
            if c not in curr.children:
                curr.children[c] = TrieNode()
            curr = curr.children[c]
        curr.endWord = True
    def search(self, word):
        curr = self.root
        cnt = 0
        for c in word:
            if c not in curr.children:
                return word
            cnt += 1
            curr = curr.children[c]
            if curr.endWord:
                return word[:cnt]
        return word
class Solution:
    def replaceWords(self, dictionary: List[str], sentence: str) -> str:
        trie = Trie()
        for word in dictionary:
            trie.insert(word)
        res = []
        for word in sentence.split(' '):
            res.append(trie.search(word))
        # print(' '.join(res))
        return ' '.join(res)
        
